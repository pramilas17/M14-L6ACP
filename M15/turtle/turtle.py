import turtle as t
t.screensize(600,600)
t.bgcolor("white")
t.color("black","red")
t.width(8)
t.begin_fill()
t.setheading(90)
t.goto(100,90)
t.pendown()
t.circle(100,180)
t.forward(200)
t.circle(40,180)
t.forward(50)
t.setheading(-90)
t.circle(20,160)
t.setheading(-90)
t.forward(50)
t.circle(40,180)
t.goto(100,90)
t.end_fill()
t.penup()
t.goto(80,60)
t.color("black","skyblue")
t.setheading(0)
t.pendown()
t.begin_fill()
t.circle(40,180)
t.forward(80)
t.circle(40,180)
t.forward(80)
t.end_fill()